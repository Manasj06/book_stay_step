/**
 * UseCase1HotelBookingApp
 * 
 * This class represents the entry point of the Hotel Booking application.
 * It demonstrates how a Java program starts execution and displays
 * a welcome message along with application details.
 * 
 * @author Manas
 * @version 1.0
 */
public class UseCase1HotelBookingApp {

    /**
     * Main method - Entry point of the Java application.
     * JVM starts execution from here.
     * 
     * @param args Command line arguments
     */
    public static void main(String[] args) {

        // Display welcome message
        System.out.println("======================================");
        System.out.println("   Welcome to Hotel Booking System   ");
        System.out.println("======================================");

        // Display application details
        System.out.println("Application Name : Hotel Booking System");
        System.out.println("Version          : v1.0");

        // End message
        System.out.println("\nApplication started successfully!");
        System.out.println("Thank you for using our service.");
    }
}